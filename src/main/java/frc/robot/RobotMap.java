package frc.robot;


/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    public static final int LEFT_MASTER = 1;
    public static final int LEFT_SLAVE_A = 1;
    public static final int LEFT_SLAVE_B = 1;
    public static final int RIGHT_MASTER = 1;
    public static final int RIGHT_SLAVE_A = 1;
    public static final int RIGHT_SLAVE_B = 1;

    public static final int XBOX = 1;
    public static final int WHEEL = 1;
    public static final int STICK = 1;
    public static final int GRABBER_BUTTON = 1;

    public static final int SPRING_SOLENOID_REVERSE= 1;
    public static final int GRAB_SOLENOID_REVERSE = 1;
    public static final int SPRING_SOLENOID_FORWARD = 1;
    public static final int GRAB_SOLENOID_FORWARD = 1;
    public static final int LEFT_WHEEL = 1;
    public static final int RIGHT_WHEEL = 1;

}
